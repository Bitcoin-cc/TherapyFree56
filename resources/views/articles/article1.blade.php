    <h1>Dizziness , trembling, pounding heartbeat - What these body sensations are telling you.</h1>

<article>
<p/>For many, the thought of even performing public speaking can make them nervous and afraid. (Random fact: Public speaking is voted one of the worst fears to experience!). However, the feeling that these people experience are not unique: everyone has felt nervous or afraid before. 
<p/>What many people do not realize, however, is that this feeling could linger on for an extended period of time. This is especially true if they are undergoing constant stress or in an environment that keeps them on their toes at all times! 
<p/>Clients are often referred to the psychologist or counselor when they report uncomfortable physical sensations to their GP, only to find that there are no underlying health conditions. Once physical health conditions are ruled out, the likely reason for this is because it is instead psychological. Clients may report having extended period of time of pounding heartbeat, trembling, restlessness/ agitation, which interrupts their day-to-day activties and affect their quality of life. 
<p/>If you often experience these physical sensations, it could mean that you are experiencing a higher than average feeling of anxiety: 
<ul>
    <li>Pounding heart/ accelerated heart rate</li>
    <li>Sweating</li>
    <li>Trembling/ shaking</li>
    <li>Shortness of breath</li>
    <li>Choking sensation</li>
    <li>Chest pain/ discomfort</li>
    <li>Nausea</li>
    <li>Dizzy/ light-headedness</li>
    <li>Chills/ heat sensations</li>
    <li>Numbness/ tingling sensations</li>
    <li>Restlessness</li>
    <li>Easily fatigued</li>
    <li>Difficulty concentrating</li>
    <li>Irritability</li>
    <li>Muscle tension</li>
    <li>Sleep disturbance</li>
</ul>
<p/>One effective way to manage these feelings is to learn how to relax! Take some time off each day to go for a walk or perform some physical exercise. Spend some time each day having positive interactions with others. Put aside a few minutes a day to perform deep breaths. These lifestyle changes will significantly imrpove how you feel! 

<p/>If you believe that these physical sensations are too overwhelming and are affecting your life too much, consider speaking to a professional who will be able to provide help and guidance in managing it effectively. The Help Talk, for example, has a network of qualified mental health professionals who will be able to assist you through this process. 

<p/>Done with this article, and moving on to the next activity? Take a deep breath :) 
